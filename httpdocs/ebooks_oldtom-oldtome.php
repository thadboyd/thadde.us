  <section id="story">
    <h2 id="story">Old Tom and the Old Tome</h2>

    <p>I’ve heard men say that history is written by the victors.</p>

    <p>I’ve yet to hear a man explain why if that’s true, the bards sing about the Old King like the sun shone out his arsehole.</p>

    <p>King Arthur. Arthur Sister-Fucker. Arthur Baby-Killer. God’s wounds. I’ve done things I’m not proud of, but I never killed no babies. Nor fucked any sisters, neither.</p>

    <p>Arthur the Sister-Fucker weren’t no victor. I was at Camlann. It was what you would call a draw.</p>

    <p>Not that I held much love for Mordred the Hunchback. Never did understand why he tried to take the throne by force. Bastard or no, he was the King’s only son, and that weren’t likely to change. If he’d been a patient man, he’d have had his throne, in time.</p>

    <p>Patience is the only way to stay alive in my line of work. Ever met a mercenary who retired and lived to a ripe old age? I aim to be the first. That’s why I didn’t go east. That’s why I’m not out there right now, fighting the New King’s war against the Turk. Not many mercs left can say we saw Camlann with our own two eyes, and even less who’ve still <em>got</em> both eyes, both arms, and both legs.</p>

    <p>I don’t see myself as a lucky man. Just a patient one. I could have gone east. Could have been a captain this time. Could have died in the Holy Land, for nothing, buried in an unmarked grave. You pick your jobs. And you don’t get to be as old as I am by picking the kind where you march off to foreign lands and try to overthrow the Sultan. I already fought a war to overthrow a King once, right here at home. Only reason I’m still alive is that I knew when the war was lost. Knew when to run, and I’m not ashamed to admit it. I still know when to run, but I also know I don’t run as fast as I used to.</p>

    <p>So I take smaller jobs now — smaller than going to war, anyway.</p>

    <p>That’s why the witch sought me out that day.</p>

    <hr/>

    <p>I was drinking at the Three-Legged Hog; that’s where I go when I’m in Brun and looking for a job. They know me there, and they know what I’m there for.</p>

    <p>I felt her before I saw her. She slid onto the stool next to me, and spoke in a deep voice. It sounded like a man’s voice, but this weren’t no man.</p>

    <p>“You’re Old Tom?” she asked me, in her man’s voice. She had not removed her hood. “I was expecting someone older.”</p>

    <p>“I’m the oldest Tom here,” I told her. “Old for my line of work. But you look like you’ve seen more winters than I have. We don’t get many of your like in the Hog — not many ladies, much less witches.”</p>

    <p>“What makes you say I’m a witch?” she asked, though she seemed none too surprised.</p>

    <p>“I know runes on a cloak when I see them,” I told her. “Even when they’re black thread on a black cloak. Even when that cloak’s got mud on it.”</p>

    <p>“You’re right,” she said, “I’m a witch. And I’m looking for something.” Seemed the introductions were over and it was time to talk about the job. That didn’t hurt my feelings, even if she hadn’t even told me her name.</p>

    <p>“What, where, when, and how much?” I asked.</p>

    <p>“A book,” she said. “North; Merlin’s Country. As soon as you can get it.” She withdrew a bag from a pocket in her robe; I opened it and counted the coins.</p>

    <p>“You know my terms?” I asked her.</p>

    <p>“Half your pay now, half on delivery.” She knew my terms, all right; knew them word-for-word. “You will do everything in your power to retrieve the item, but if you fail to do so, you will still keep the first half of your payment.” So far so good. “In return for this show of good faith, you agree not to sell the item to any third party, no matter the offer, unless I am unwilling or unable to pay you the other half of your fee.”</p>

    <p>“You know the words,” I said. “Now finish them.”</p>

    <p>“I will tell you everything you need to know in order to get my item and bring it back safely,” she said. She drew two rolls of parchment from another pocket, and unrolled and flattened the first. It was a drawing of a book.</p>

    <p>“That’s what I’m looking for, is it?” I peered at the drawing. It looked to be thick and fancy, with ridges around the edges and a clasp across the front. The cover said <em>The Four Elements</em>, though I didn’t let on that I could tell; I know my letters well enough, but I like it better when people don’t know that.</p>

    <p>“That’s what you’re looking for,” she echoed. “Tom, can you read a map?” I nodded, and she laid out the second parchment. “This is the Dark Forest. Do you know it?”</p>

    <p>“Not well,” I said, “but I know someone who does, and I know where to find him.”</p>

    <p>“Good.” She pointed at the X marking the map. “This is where I think the book is. There used to be a school there.”</p>

    <p>“What kind of school?” I asked. “And what kind of book?”</p>

    <p>“A school of magic and a book of magic.” This was good. She was answering my questions, no hesitation.</p>

    <p>“Why do you want the book?” I asked. “And what makes you think it’s still there, if the school is gone?”</p>

    <p>“I want the book because magic is fading from the world of men,” she said. “Merlin is gone. The Fay-Folk have crossed the sea. The last of the dragons have died — both the great wyrms and the line of kings who bore their name. I seek the book because I do not want to see the Craft die out.</p>

    <p>“As for how I know the book is still there? It is protected by wards. It will not have perished when the school was destroyed, nor been taken by looters, nor succumbed to the elements. I have a charm which will reveal it to you, should you accept this task. Without the charm, none will ever find it.”</p>

    <p>There was one more question she hadn’t answered. “Why do you need me? You know where the book is. Why not retrieve it yourself?”</p>

    <p>“The school had defenses.” She seemed to hesitate a bit before that last word. “Magic defenses. When it was destroyed, their arcane energies were sapped. There is magic in the books, and in the ruins, and in the charm I will give you. But it will not be enough to reawaken the guardians. But if someone with the Gift approached them, then reawaken they surely would. My Craft would work against me there. I need someone who is free of magic.”</p>

    <p>I looked her over. I thought about it. She seemed to be telling the truth. I drained my glass.</p>

    <hr/>

    <p>Rolf the Hunter looked, smelled, ate, and laughed like a goat. He was not hard to find. Or I should say it was not hard for him to find me. I set up camp that first night, at the edge of the Dark Forest, and he came bounding up to greet me. I know he could have stole right up behind me, quiet as you please, but he knew a friend when one came calling.</p>

    <p>“Ho, Old Tom!” he called. “What brings you to my forest?”</p>

    <p>I offered him a seat by the fire and a skin of wine; he offered me a rabbit for my supper. And I told him my tale.</p>

    <p>“Aye, I know the place,” he told me. “Animals don’t like it much. Grass and moss and ivy like it just fine.”</p>

    <p>“I expected such,” I agreed. “I left old Gnash stabled back in town.”</p>

    <p>“Good old Gnash!” Rolf laughed. “Give him an apple for me when you see him again.”</p>

    <p>The fire crackled and Rolf told me tales. He apologized for the <em>meager fare</em> he had supplied for my supper, and told me of days of plenty and the giant boar he had once slain that kept him in salted pork for a month. Next morn, we awoke and he told me the tale of the day he’d spent fishing, only to have a hawk swoop down and make off with his trout, with the line and pole still attached. He led me on into the forest, and he told me the tale of the time a bear had caught him without bow or knife and he had been forced to wrestle it. He showed me the scar. I wondered, sometimes, what Rolf did during the long seasons when there were no other men about to tell his tales to, and I think perhaps he told them to the trees and to the beasts. I pictured him skinning the deer whose hide he wore, and telling it the tale of the deer he had shot to skin and tan and make a coat of.</p>

    <p>On foot it was three days’ journey into the Dark Forest to the place the school had stood. Three days of Rolf telling me his tales, until all at once he fell silent and I could feel the hairs stand up on the back of my neck.</p>

    <p>“This is it,” he said, like he needed to say it.</p>

    <p>I nodded and I reached into my pocket and pulled out the necklace the witch had given me. I hung it around my neck. I didn’t feel any different.</p>

    <p>There were scorch marks on some of the trees, and stones scattered about, some of them scorched too. I couldn’t tell if the school had been destroyed from within or from without, but in any case it weren’t destroyed by no ordinary fire or ram.</p>

    <p>The form of the school loomed ahead, what there was of it. It didn’t look like it had been much of a building when it had stood; it sure weren’t the Tower of Merlin from the stories. It looked like it had been one story and just a few long rooms.</p>

    <p>Yet the air about it still crackled with something, something that was keeping the animals away. And there had been guardians, sure enough; we saw two stone gargoyles on the approach. One had been cracked into a thousand pieces so’s you could hardly tell what it used to be, but the one next to it still looked like a gargoyle — knocked over on its side, with maybe some fingers and toes missing, but still eerie in that way that it didn’t feel like it should be here in the forest.</p>

    <p>We walked past it. Even Rolf couldn’t think of nothing to say; he just gave a low whistle.</p>

    <p>The front doorway to the school still stood, but part of the wall and most of the roof had caved in. There was a pile of rubble just inside and I didn’t know if I’d be able to squeeze past it. But I could hear somebody singing on the other side.</p>

    <p>“Rolf,” I said, soft as you please, “do you hear singing?”</p>

    <p>“No, Tom,” he said. “I hear creaking.”</p>

    <p>And then I heard it too.</p>

    <p>I turned back, slowly, and I saw it: the gargoyle, the one that was still one piece, was starting to flex its wings and its arms, and push itself up off its side.</p>

    <p>“That bitch lied to me,” I cursed. “The guardians are still active.” I threw my pack to the ground and drew my sword. The singing seemed louder, but that weren’t my concern right then.</p>

    <p>Rolf unslung his bow and loosed an arrow, quick as a flash. His aim was true; he hit the beastie right between the eyes. For all the good it did. It weren’t the sort of arrowhead that could pierce stone or enchantment.</p>

    <p>“Run!” I screamed, and I did. I didn’t see the gargoyle leap at Rolf, I just felt the earth shake when it landed. I heard him choke, and I heard a crack, and I hoped it killed him quick because I knew it hadn’t gave him the sort of wound that was going to get better.</p>

    <p>I was already in the doorway, hacking at thatch and frantically moving stones, seeking to make a big enough gap that I could squeeze inside. The music was coming from inside, and I had a feeling I knew what it was.</p>

    <p>I sucked in my belly and just squeezed in. The beast had done with Rolf now and was on my heels, but it were bigger than me. I heard it snarl as it started tearing at the stones and heaving them away, to clear a path so it could follow me.</p>

    <p>I followed the song. Into the room, ’round collapsed beams, through straw and dust and rubble. And there it was. The book called to me, and I was going to have it if it was the last thing I ever did. Which seemed likely.</p>

    <p>The gargoyle was through the doorway. The book was in my hand. It flew open, it showed me a page, and I started reading.</p>

    <p>The wind picked up. Not from outside, but inside the half-collapsed walls of the school.</p>

    <p>The gargoyle lunged at me, but it couldn’t push against the wind. I kept reading. I didn’t know these words, and I couldn’t tell you today what they were. But I could read them, and I did. The wind pushed the gargoyle back, and back, and then slammed it into the wall.</p>

    <p>I reached the end of the page and I started back over. The wind kept up and it slammed the gargoyle into the stones again, and again. Its teeth rattled and its joints creaked, and it fell to pieces.</p>

    <p>The wind stopped, and all I could hear was the blood pounding in my ears and the sound of my own gasping breathing. I curled up into a ball and whimpered, I’m not ashamed to admit it, and I don’t know how long I stayed there.</p>

    <p>I carried Rolf far enough that I couldn’t feel the tingle of the magic anymore, and I buried him. I built up a cairn of stones and leaned his bow against it. I think he’d have liked it.</p>

    <p>I buried the gargoyle, too, the parts of it that were small enough for me to move. It felt like something had passed out of the world, never to return, and I felt a sadness for that. I think Rolf would have liked that too, strange as that may sound. Way I figure it, it was just a dumb beast; he wouldn’t blame it for doing what it did any more than he’d blame a bird or a bear or an Irishman. I know that, had he lived to tell his tale, he’d have told it loud and often to every tree, fern, and mushroom in the Dark Forest.</p>

    <p>I packed the book, and as many other books as I could find in that school and fit in my pack, and I took leave of that place.</p>

    <hr/>

    <p>I had plenty of time to think, before I got to meet the witch again to give her what she’d asked for. I kept thinking on what I’d shouted when that gargoyle woke up on us: <em>That bitch lied to me. The guardians are still active.</em> If that were so, then I would find a way to make her pay, witch or no. But I weren’t so sure, anymore. My gut was telling me she’d have been just as surprised as I was.</p>

    <p>And so when we sat back down to settle up, back at the Three-Legged Hog where we had first met, I asked her.</p>

    <p>“Did you know that gargoyle was going to wake up and come at us?” I asked her, soft like, and I watched her face.</p>

    <p>“No.” She didn’t look surprised the way most people look surprised, but she looked curious. I believed her.</p>

    <p>“Did you know I could read the book?” I asked her. I opened it up and showed her the page I’d read; I would never forget that page. “Did you know I could cast this spell?”</p>

    <p>“Tom,” she said, “I didn’t even know you could <em>read</em>.”</p>

    <p>“So I can do magic,” I said. “And I didn’t know it, and <em>you</em> didn’t know it.”</p>

    <p>She closed her eyes. She seemed to be thinking, feeling. It felt like something brushed me but she hadn’t moved.</p>

    <p>“It’s small,” she said at last. “You have the spark within you, but it’s small. Too small to feel it, unless I was looking for it. And no, I wasn’t looking for it when I met you. I didn’t see any reason to.”</p>

    <p>“A good man died.” I weren’t accusing her, but she should know. “But it sounds like that weren’t nobody’s fault. We just didn’t know no better.”</p>

    <p>The witch sighed. “No, but it was my mistake. I’ll up your pay, of course; not that it will make up for losing your friend, but you risked more than I knew.”</p>

    <p>I nodded, slowly. “I don’t want no more coin,” I said at last. “But I been thinking about that school. And I been thinking about your problem, about how you don’t want to see magic go away. Well, here I am — I may not be much, almost forty, slowing down, can’t hardly read, and last month I didn’t even know I could cast a spell. But I’m right in front of you. And I want you to teach me.”</p>

